package com.company;

public class A_02_Literals {
    public static void main(String[] args) {
        // Literal is nothing but just value on the right side ex: 34, 56, 'A' , 5.6
        byte age = 34;
        int age2 = 56;
        long age3 = 87;
        long ageDino = 566666666666666L;  // L or l is for long int
        char ch = 'A';
        float f1 = 5.6f;   // f or F for float
        double d1 = 4.66D;  // D or d for double
        boolean a = true;
        System.out.println(age);
        String str = "ABHI";
        System.out.println(str);
    }
}
